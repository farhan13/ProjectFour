
public class Coordinates {

	private int X;
	private int Y;
	
	public Coordinates(int x, int y){
		
		X = x;
		Y = y;
		
	}
	
	public int getX(){
		
		return X;
		
	}
	
	public int getY(){
		
		return Y;
		
	}
	
	public void setX(int new_x){
		
		X = new_x;
	}
	
	public void setY(int new_y){
		
		X = new_y;
	}
	
	
}
